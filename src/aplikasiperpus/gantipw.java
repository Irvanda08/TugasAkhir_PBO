    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:                                        
        String username = user.getText();
        String oldPass = new String(oldPassword.getPassword());
        String newPass = new String(newPassword.getPassword());
        String cekuser = null;
        String cekpassword = null;
        try {
            Connection connect = koneksi.getKoneksi();
            Statement st = (Statement) connect.createStatement();
            String sql = "SELECT * FROM staff WHERE username = '"+username+"' AND password = '"+oldPass+"'";
            java.sql.ResultSet res = st.executeQuery(sql);
            while(res.next()){
                cekuser = res.getString("username");
                cekpassword = res.getString("password");
            }
            res.close();
            st.close();
        } catch (SQLException e){
            System.out.print("Terjadi Error");
        }
        if (cekuser == null && cekpassword == null) {
            String pesan = "Username atau Password Salah";
            JOptionPane.showMessageDialog(null, pesan, "Message", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                Connection connect = koneksi.getKoneksi();
                Statement st = (Statement) connect.createStatement();
                String updateQuery = "UPDATE staff SET password = '" + newPass + "' WHERE username = '" + username + "'";
                st.executeUpdate(updateQuery);
                st.close();

                String pesan1 = "Password Berhasil Diubah";
                JOptionPane.showMessageDialog(null, pesan1, "Message", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (SQLException e) {
                System.out.print("Terjadi Error");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

